		<h2>1. Qu'est-ce qu'une montre connectée ?</h2>
  <p>Une montre connectée est un appareil portable porté au poignet qui offre des fonctionnalités similaires à celles d’un smartphone : notifications, appels, suivi de la santé et plus encore.</p>  <h2>2. Pourquoi acheter une montre connectée ?</h2>
  <ul>
    <li>Suivi de la santé et des activités (pas, sommeil, fréquence cardiaque...)</li>
    <li>Notifications d’appels, messages, réseaux sociaux</li>
    <li>Contrôle de la musique, de la caméra, domotique</li>
    <li>Navigation GPS, paiement sans contact</li>
  </ul>  
		<h2>3. Types de montres connectées</h2>
  <ul>
