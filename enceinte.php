  <h2>1. Qu'est-ce qu'une enceinte intelligente ?</h2>
  <p>Une enceinte intelligente est un haut-parleur connecté doté d’un assistant vocal. Elle permet de jouer de la musique, contrôler des appareils domotiques, poser des questions et bien plus encore.</p>
		<h2>2. Principaux assistants vocaux</h2>
  <ul>
    <li><strong>Google Assistant :</strong> intégré dans les enceintes Google Nest et autres</li>
    <li><strong>Amazon Alexa :</strong> utilisé par les enceintes Echo</li>
    <li><strong>Apple Siri :</strong> disponible via le HomePod</li>
  </ul>
		<h2>3. Fonctionnalités courantes</h2>
  <ul>
    <li>Commandes vocales pour musique, météo, alarmes, actualités</li>
    <li>Contrôle des objets connectés (lumières, thermostats, etc.)</li>
    <li>Streaming via Wi-Fi, Bluetooth ou AirPlay</li>
    <li>Multiroom (synchronisation avec d'autres enceintes)</li>
  </ul>
