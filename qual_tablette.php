			🚀 Milieu de gamme : Qualcomm Snapdragon 7xx, Exynos 1380 (bureautique, multimédia).<br />
			🔥 Haut de gamme : Apple M1/M2, Snapdragon 8 Gen 2/3 (gaming, création graphique).
